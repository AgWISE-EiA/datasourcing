    GPS_fieldData <- readRDS(paste("~/agwise-datacuration/dataops/datacuration/Data/useCase_",country, "_",useCaseName, "/", Crop, "/result/compiled_fieldData.RDS", sep=""))  
    countryCoord <- unique(GPS_fieldData[, c("lon", "lat", "plantingDate", "harvestDate", ID)])
    countryCoord <- countryCoord[complete.cases(countryCoord), ]
    names(countryCoord) <- c("longitude", "latitude", "plantingDate", "harvestDate", ID)
    ground <- countryCoord
  }
  
  
  # 2. the ground data ####
  ground$Planting <- as.Date(ground$plantingDate, "%Y-%m-%d") # Planting date in Date format
  ground$Harvesting <- as.Date(ground$harvestDate, "%Y-%m-%d") # Harvesting date in Date format
  
  countryShp <- geodata::gadm(country, level = 3, path='.')
  dd2 <- raster::extract(countryShp, ground[, c("longitude", "latitude")])[, c("NAME_1", "NAME_2")]
  ground$NAME_1 <- dd2$NAME_1
  ground$NAME_2 <- dd2$NAME_2
  
  
  if(AOI == TRUE){
    
    ### 3.1.1 Convert planting Date and harvesting in Julian Day ####
    pl_j <-as.POSIXlt(unique(ground$Planting))$yday
    hv_j <-as.POSIXlt(unique(ground$Harvesting))$yday
    
    if (planting_harvest_sameYear ==  TRUE) {
      
      cls <- makeCluster(jobs)
      doParallel::registerDoParallel(cls)
      
      ### 3.1.2 Read for the corresponding year and date
      RelativeHumidity_result <- foreach(i=1:length(listRaster_RH), .packages = c('terra', 'plyr', 'stringr','tidyr')) %dopar% {
        rast1 <- listRaster_RH[i]
        source("~/agwise-datasourcing/dataops/datasourcing/Scripts/generic/get_geoSpatialRelativeHumidity_MC.R", local = TRUE)
        summary_pointdata_RH(rastLayer1=rast1, rastLayer2=NULL, gpsdata = ground, pl_j=pl_j, hv_j=hv_j, planting_harvest_sameYear = TRUE)
      }
      RelativeHumidity_points <- do.call(rbind, RelativeHumidity_result)
      
      stopCluster(cls)
    }
    
    if (planting_harvest_sameYear ==  FALSE) {
      cls <- makeCluster(jobs)
      doParallel::registerDoParallel(cls)
      
      ## RelativeHumidity
      RH_result2 <- foreach(i = 1:(length(listRaster_RH)-1), .packages = c('terra', 'plyr', 'stringr','tidyr')) %dopar% {
        listRaster_RH <- listRaster_RH[order(listRaster_RH)]
        source("~/agwise-datasourcing/dataops/datasourcing/Scripts/generic/get_geoSpatialRelativeHumidity_MC.R", local = TRUE)
        rast1 <- listRaster_RH[i]
        rast2 <- listRaster_RH[i+1]
        summary_pointdata_RH(rastLayer1=rast1, rastLayer2=rast2, gpsdata = ground, pl_j=pl_j, hv_j=hv_j, planting_harvest_sameYear = TRUE)
      }
      RelativeHumidity_points <- do.call(rbind, RH_result2)
      
      stopCluster(cls)
      
    }
    
  }else {## when the planting and harvest dates varies for every row of data because it is actual trial data
    
    
    # 3 Loop on all the ID to calculate the parameters ####
    RelativeHumidity_points <- NULL
    for(i in 1:nrow(ground)){
      print(i)
      # Extract the information for the i-th row
      groundi <- ground[i,]
      
      # Test if the cropping season overlaps two civil year
      yearPi <- format(as.POSIXlt(groundi$Planting), "%Y")
      yearHi <- format(as.POSIXlt(groundi$Harvesting), "%Y")
      ### 3.2.1 Convert planting Date and harvesting in Julian Day ####
      pl_j <-as.POSIXlt(groundi$Planting)$yday
      hv_j <-as.POSIXlt(groundi$Harvesting)$yday
      
      ## 3.1 Case same year ####
      if (yearPi == yearHi) {
        ### 3.1.2 Read for the corresponding year and date ####
        ## RelativeHumidity
        RHi <-listRaster_RH[which(grepl(yearPi, listRaster_RH, fixed=TRUE) == T)]
        RHi <- terra::rast(RHi, lyrs=c(pl_j:hv_j))
      }
      
      ## 3.2 Case two years ####
      if (yearPi < yearHi) {
        
        ### 3.2.2 Read for the corresponding years and date ####
        ## RelativeHumidity
        RHi1<-listRaster_RH[which(grepl(yearPi, listRaster_RH, fixed=TRUE) == T)]
        RHi1 <- terra::rast(RHi1, lyrs=c(pl_j:terra::nlyr(terra::rast(RHi1))))
        RHi2 <-listRaster_RH[which(grepl(yearHi, listRaster_RH, fixed=TRUE) == T)]
        RHi2 <- terra::rast(RHi2, lyrs=c(1:hv_j))
        RHi <- c(RHi1, RHi2)
        
      }
      
      ### 3.3 Extract the information for the i-th row ####
      xy <- groundi[, c("longitude", "latitude")]
      xy <- xy %>%
        mutate_if(is.character, as.numeric)
      
      RelativeHumidity_points_i <- terra::extract(RHi, xy,method='simple', cells=FALSE)
      RelativeHumidity_points_i <- RelativeHumidity_points_i[, -1]
      
      ## get year
      groundi$Year <- yearPi
      
      # Compute the total amount of RelativeHumidity
      groundi$AvRelativeHumidity <- round(mean(as.numeric(RelativeHumidity_points_i[c(1:length(RelativeHumidity_points_i))])), digits=1)
      
      # Compute monthly RelativeHumidity, at 31 days interval and the remaining  days at the end
      mdiv <- unique(c(seq(1, length(RelativeHumidity_points_i), 30), length(RelativeHumidity_points_i)))
      
      mrf <- c()
      for (k in 1:(length(mdiv)-1)) {
        if(k == 1){
          mrf <- c(mrf, mean(as.numeric(RelativeHumidity_points_i[c(mdiv[k]:mdiv[k+1])])))
        }else{
          mrf <- c(mrf, mean(as.numeric(RelativeHumidity_points_i[c((mdiv[k]+1):(mdiv[k+1]))])))
        }
      }
      
      if(length(mrf) > 12){## if the crop is > 6 month on the field
        mrf <- c(mrf, rep("NA", 12 -length(mrf)))
      }
      
      mrf_names <- c(paste0("RH_month", c(1:12)))
      for (h in 1:length(mrf_names)) {
        colname <- mrf_names[h]
        groundi[[colname]] <- mrf[h]
      }
      
      groundi <- subset(groundi, select=-c(Planting, Harvesting, Year))
      RelativeHumidity_points <- rbind(RelativeHumidity_points, groundi)
    }
  }
  
  RelativeHumidity_points <- RelativeHumidity_points %>% 
    select_if(~sum(!is.na(.)) > 0)
  
  
  # 4 Writting of output: Check if the directory exists
  Planting_month_date <- gsub("-", "_", Planting_month_date)
  
  fname_RH <- ifelse(AOI == "TRUE", paste("RelativeHumidity_summaries_AOI_" ,Planting_month_date, "_", dataSource, ".RDS",sep=""), paste("RelativeHumidity_summaries_trial_", dataSource, ".RDS", sep=""))
  
  saveRDS(object = RelativeHumidity_points, file=paste(pathOut1, fname_RH, sep="/"))
  saveRDS(object = RelativeHumidity_points, file=paste(pathOut2, fname_RH, sep="/"))
  saveRDS(object = RelativeHumidity_points, file=paste(pathOut3, fname_RH, sep="/"))  
  
  return(RelativeHumidity_points)
  
}





# Extract RelativeHumidity data time series for point based data prepared for Crop Models
#' @description this functions loops through all .nc files (~30 - 40 years) for Solar Radiation and provide point based data.
#' @details for AOI it requires a "AOI_GPS.RDS" data frame with c("longitude","latitude") columns being saved in 
#'                            paste("~/agwise-datasourcing/dataops/datasourcing/Data/useCase_", country, "_",useCaseName, "/", Crop, "/raw", sep="") 
#'          for trial sites it requires a "compiled_fieldData.RDS" data frame with c("lon", "lat", "plantingDate", "harvestDate") being saved in 
#'                    paste("~/agwise-datacuration/dataops/datacuration/Data/useCase_",country, "_",useCaseName, "/", Crop, "/result", sep="")
#  
#' @param country country name
#' @param useCaseName use case name  name
#' @param Crop the name of the crop to be used in creating file name to write out the result.
#' @param AOI True if the data is required for target area, and false if it is for trial sites
#' @param overwrite default is FALSE 
#' @param Planting_month_date is needed only for AOI and should be provided as month_date, for trial locations the actual planting date is be used so no need to change the default value
#' @param Harvest_month_date is needed only for AOI and should be provided as month_date, for trial locations the actual harvest date is be used so no need to change the default value
#' @param jobs defines how many cores to use for parallel data sourcing
#' @param dataSource is one of c("CHIRPS", "AgEra")
#' @param ID only when AOI  = FALSE, it is the column name Identifying the trial ID in compiled_fieldData.RDS
#' 
#' @return a data frame with file name made to reflect point/summary, AOI/trial,  planting as mm_dd and source CHIRPS/AgEra
#' the data frame contains daily RelativeHumidity for every GPS point. 
#' For the trial sites: it provides:longitude, latitude, plantingDate and harvestDate as yyyy-mm-dd, yearPi & yearHi as yyyy, 
#' pl_j & hv_j as date of the year for panting & harvest, growinglength and daily RelativeHumidity by date, NA for dates not part of the growing season for a trial
#' For AOI: it provides dily RelativeHumidity for ll dates between Planting_month_date and Harvest_month_dates plus 
#' plantingYear, harvestYear, longitude, latitude   
#' 
#' @examples: get_summaries(country = "Rwanda",  useCaseName = "RAB", Crop = "Potato", AOI = FALSE, overwrite = TRUE,
#'              Planting_month_date = "07-01",  Harvest_month_date = "11-30", jobs=10, id = "TLID")
get_geoSpatial_4CropModels <- function(country, useCaseName, Crop, AOI = FALSE, overwrite = FALSE, 
                               Planting_month_date = "02-01", Harvest_month_date = "05-30", 
                               jobs = 10, dataSource, ID, varName = NULL){
  
  # 4.1. Initialization of input and output data ####
  
  # Input RelativeHumidity
  if(dataSource == "chirts"){
    listRaster_RH <-list.files(path="/home/jovyan/agwise-datasourcing/dataops/datasourcing/Data/Global_GeoData/Landing/", varName,"/chirts", pattern=".nc$", full.names = TRUE)
  }else{
    listRaster_RH <-list.files(path="/home/jovyan/agwise-datasourcing/dataops/datasourcing/Data/Global_GeoData/Landing/", varName,"/AgEra", pattern=".nc$", full.names = TRUE)
    
  }
  
  
  # Creation of the output dir
  pathOut1 <- paste("/home/jovyan/agwise-datasourcing/dataops/datasourcing/Data/useCase_", country, "_",useCaseName, "/", Crop, "/result/",varName, sep="")
  pathOut2 <- paste("/home/jovyan/agwise-potentialyield/dataops/potentialyield/Data/useCase_", country, "_",useCaseName, "/", Crop, "/raw/",varName, sep="")
  
 
  if (!dir.exists(pathOut1)){
    dir.create(file.path(pathOut1), recursive = TRUE)
  }
  
  if (!dir.exists(pathOut2)){
    dir.create(file.path(pathOut2), recursive = TRUE)
  }
  
  
  
  # Input point data AOI / Trial
  if(AOI == TRUE){
    countryCoord <- readRDS(paste("~/agwise-datacuration/dataops/datacuration/Data/useCase_", country, "_",useCaseName, "/", Crop, "/result/AOI_GPS.RDS", sep=""))
    
    # countryCoord <- readRDS(paste("~/agwise-datasourcing/dataops/datasourcing/Data/useCase_", country, "_",useCaseName, "/", Crop, "/raw/AOI_GPS.RDS", sep=""))
    countryCoord <- unique(countryCoord[, c("longitude", "latitude")])
    countryCoord <- countryCoord[complete.cases(countryCoord), ]
    
    ## check if both planting and harvest dates are in the same year
    Planting_month <- as.numeric(str_extract(Planting_month_date, "[^-]+"))
    harvest_month <- as.numeric(str_extract(Harvest_month_date, "[^-]+"))
    if(Planting_month < harvest_month){
      planting_harvest_sameYear <- TRUE
    }else{
      planting_harvest_sameYear <- FALSE
    }
    
    if (planting_harvest_sameYear ==TRUE){ #is used only to get the date of the year so the years 2001 and 2002 have no value except for formating 
      countryCoord$plantingDate <- paste(2001, Planting_month_date, sep="-")
      countryCoord$harvestDate <- paste(2001, Harvest_month_date, sep="-")
    }else{
      countryCoord$plantingDate <- paste(2001, Planting_month_date, sep="-")
      countryCoord$harvestDate <- paste(2002, Harvest_month_date, sep="-")
    }
    countryCoord <- countryCoord[complete.cases(countryCoord), ]
    ground <- countryCoord[, c("longitude", "latitude", "plantingDate", "harvestDate")]
    
  }
  
  
  
  ground$Planting <- as.Date(ground$plantingDate, "%Y-%m-%d") # Planting date in Date format
  ground$Harvesting <- as.Date(ground$harvestDate, "%Y-%m-%d") # Harvesting date in Date format
  
  countryShp <- geodata::gadm(country, level = 3, path='.')
  dd2 <- raster::extract(countryShp, ground[, c("longitude", "latitude")])[, c("NAME_1", "NAME_2")]
  ground$NAME_1 <- dd2$NAME_1
  ground$NAME_2 <- dd2$NAME_2
  
  # 4.2. Compute the seasonal RelativeHumidity parameters for AOI ####
  
  if(AOI == TRUE){
    
    # Convert planting Date and harvesting in Julian Day 
    pl_j <-as.POSIXlt(unique(ground$Planting))$yday
    hv_j <-as.POSIXlt(unique(ground$Harvesting))$yday
    
    ## 4.2.1. Case planting and harvesting dates span the same year ####
    if (planting_harvest_sameYear ==  TRUE) {
      
      cls <- makeCluster(jobs)
      doParallel::registerDoParallel(cls)
      
      # Loop on all the year 
      rf_4CP <- foreach(i=1:length(listRaster_RH), .packages = c('terra', 'plyr', 'stringr','tidyr')) %dopar% {
        rasti <- listRaster_RH[i]
        PlHvD <- terra::rast(rasti, lyrs=c(pl_j:hv_j))
        xy <- ground[, c("longitude", "latitude")]
        raini <- terra::extract(PlHvD, xy, method='simple', cells=FALSE)
        raini <- raini[,-1]
        
        ## name the columns by date in the form of dd_mm_yyyy
        rainyDssat <- raini
        dates_nr <- sub("^[^_]+", "", names(rainyDssat))
        dates_nr <- as.numeric(gsub("_", "", dates_nr))
        year_nr <- str_extract(rasti, "[[:digit:]]+")
        startday <- paste(strsplit(Planting_month_date, "-")[[1]][2], strsplit(Planting_month_date, "-")[[1]][1], year_nr, sep="_")
        
        cnames <- startday
        for(h in 1:length(dates_nr)){
          dd <- as.character(as.Date(h, origin = paste(year_nr, strsplit(Planting_month_date, "-")[[1]][1], strsplit(Planting_month_date, "-")[[1]][2], sep="-")))
          cnames <- c(cnames, paste(strsplit(dd, "-")[[1]][3], strsplit(dd, "-")[[1]][2], strsplit(dd, "-")[[1]][1], sep="_"))
        }
        names(rainyDssat) <- cnames[-length(cnames)]
        
        rainyDssat_t <- as.data.frame(t(rainyDssat))
        colnames(rainyDssat_t) <- paste("Point", c(1:ncol(rainyDssat_t)), sep="_")
        rainyDssat_t$MetaDVar <- rownames(rainyDssat_t)
        return(rainyDssat_t)
        # CP <- cbind(ground, rainyDssat_t)
      }
      
      RelativeHumidity_points <- do.call(rbind, rf_4CP)
      stopCluster(cls)
      
      metadata_D <- as.data.frame(t(data.frame(longitude = ground$longitude, latitude = ground$latitude,
                                               NAME_1 = ground$NAME_1, NAME_2 = ground$NAME_2)))
      metadata_D$MetaDVar <- rownames(metadata_D)
      colnames(metadata_D) <- colnames(RelativeHumidity_points)
      RelativeHumidity_points <- rbind(metadata_D, RelativeHumidity_points)
    }
    
    ## 4.2.2. Case planting and harvesting dates span two different years ####
    if (planting_harvest_sameYear ==  FALSE) {
      
      cls <- makeCluster(jobs)
      doParallel::registerDoParallel(cls)
      ## RelativeHumidity
      rf_4CP <- foreach(i = 1:(length(listRaster_RH)-1), .packages = c('terra', 'plyr', 'stringr','tidyr')) %dopar% {
        listRaster_RH <- listRaster_RH[order(listRaster_RH)]
        rast1 <- listRaster_RH[i]
        rast2 <- listRaster_RH[i+1]
        rasti1 <- terra::rast(rast1, lyrs=c(pl_j:terra::nlyr(terra::rast(rast1))))
        rasti2 <- terra::rast(rast2, lyrs=c(1:hv_j))
        PlHvD <- c(rasti1, rasti2)
        xy <- ground[, c("longitude", "latitude")]
        raini <- terra::extract(PlHvD, xy, method='simple', cells=FALSE)
        raini <- raini[,-1]
        
        ## name the columns by date in the form of dd_mm_yyyy
        rainyDssat <- raini
        dates_nr <- sub("^[^_]+", "", names(rainyDssat))
        dates_nr <- as.numeric(gsub("_", "", dates_nr))
        year_nr <- str_extract(rasti, "[[:digit:]]+")
        startday <- paste(strsplit(Planting_month_date, "-")[[1]][2], strsplit(Planting_month_date, "-")[[1]][1], year_nr, sep="_")
        
        cnames <- startday
        for(h in 1:length(dates_nr)){
          dd <- as.character(as.Date(h, origin = paste(year_nr, strsplit(Planting_month_date, "-")[[1]][1], strsplit(Planting_month_date, "-")[[1]][2], sep="-")))
          cnames <- c(cnames, paste(strsplit(dd, "-")[[1]][3], strsplit(dd, "-")[[1]][2], strsplit(dd, "-")[[1]][1], sep="_"))
        }
        names(rainyDssat) <- cnames[-length(cnames)]
        
        rainyDssat_t <- as.data.frame(t(rainyDssat))
        colnames(rainyDssat_t) <- paste("Point", c(1:ncol(rainyDssat_t)), sep="_")
        rainyDssat_t$MetaDVar = rownames(rainyDssat_t)
        return(rainyDssat_t)
      }
      RelativeHumidity_points <- do.call(rbind, rf_4CP)
      stopCluster(cls)
      
      metadata_D <- as.data.frame(t(data.frame(longitude = ground$longitude, latitude = ground$latitude,
                                               NAME_1 = ground$NAME_1, NAME_2 = ground$NAME_2)))
      metadata_D$MetaDVar <- rownames(metadata_D)
      colnames(metadata_D) <- colnames(RelativeHumidity_points)
      RelativeHumidity_points <- rbind(metadata_D, RelativeHumidity_points)
    }
    
    
    # 4.3. Compute the seasonal RelativeHumidity parameters for trial data ####
    # when the planting and harvest dates varies for every row of data because it is actual trial data
  }else {
    
    ## 4.3.1. Get the planting and harvesting dates ####
    # Get the Year
    ground$yearPi <- format(as.POSIXlt(ground$Planting), "%Y")
    ground$yearHi <- format(as.POSIXlt(ground$Harvesting), "%Y")
    
    # Convert planting date and harvesting date in Julian Day
    ground$ pl_j <-as.POSIXlt(ground$Planting)$yday
    ground$hv_j <-as.POSIXlt(ground$Harvesting)$yday
    
   
    ##Loop on all the trial location to calculate the seasonal RelativeHumidity parameters ####
    
    RelativeHumidity_points <- NULL
    for(i in 1:nrow(ground)){
      print(i)
      
      # Extract the i-th row
      groundi <- ground[i, c("longitude", "latitude", "plantingDate", "harvestDate",ID,"NAME_1", "NAME_2","yearPi", "yearHi", "pl_j", "hv_j")]
      
      # Extract the Year 
      yearPi <- groundi$yearPi
      yearHi <- groundi$yearHi
      
      # Convert planting Date and harvesting in Julian Day
      pl_j <- groundi$pl_j
      hv_j <- groundi$hv_j
      
      
      ### 4.3.2.1. Subset the RelativeHumidity data according to the length of the growing season ####
      # Case planting and harvesting dates span the same year
      
      if (yearPi == yearHi) {
        rasti<-listRaster_RH[which(grepl(yearPi, listRaster_RH, fixed=TRUE) == T)]
        rasti <- terra::rast(rasti, lyrs=c(pl_j:hv_j))
      }
      
      # Case planting and harvesting dates span two different years
      if (yearPi < yearHi) {
        rasti1<-listRaster_RH[which(grepl(yearPi, listRaster_RH, fixed=TRUE) == T)]
        rasti1 <- terra::rast(rasti1, lyrs=c(pl_j:terra::nlyr(terra::rast(rasti1))))
        rasti2 <-listRaster_RH[which(grepl(yearHi, listRaster_RH, fixed=TRUE) == T)]
        rasti2 <- terra::rast(rasti2, lyrs=c(1:hv_j))
        rasti <- c(rasti1, rasti2)
        
      }
      
      ### 4.3.2.2.Extract the information for the i-th row ####
      
      xy <- groundi[, c("longitude", "latitude")]
      xy <- xy %>%
        mutate_if(is.character, as.numeric)
      
      raini <- terra::extract(rasti, xy,method='simple', cells=FALSE)
      raini <- raini[,-1]
      
      ##
      
      ## name the columns by date in the form of dd_mm_yyyy
      rainyDssat <- raini
      dates_nr <- sub("^[^_]+", "", names(rainyDssat))
      dates_nr <- as.numeric(gsub("_", "", dates_nr))
      
      
      plantingDate <- as.character(groundi$plantingDate)
      startday <- paste(strsplit(plantingDate, "-")[[1]][3], strsplit(plantingDate, "-")[[1]][2], yearPi, sep="_")
      cnames <- startday
      for(h in 1:length(dates_nr)){
        dd <- as.character(as.Date(h, origin = groundi$plantingDate))
        cnames <- c(cnames, paste(strsplit(dd, "-")[[1]][3], strsplit(dd, "-")[[1]][2], strsplit(dd, "-")[[1]][1], sep="_"))
      }
      names(rainyDssat) <- cnames[-length(cnames)]
      
      rainyDssat_t <- as.data.frame(t(rainyDssat))
      colnames(rainyDssat_t) <- paste("Point", i, sep="_")
      rainyDssat_t$MetaDVar <- rownames(rainyDssat_t)
      
      
      if(i == 1){
        RelativeHumidity_points <- rainyDssat_t
      }else{
        RelativeHumidity_points <- merge(RelativeHumidity_points, rainyDssat_t, by="MetaDVar", all=TRUE)
      }
    }
    
    metadata_D <- as.data.frame(t(data.frame(longitude = ground$longitude, latitude = ground$latitude,
                                             NAME_1 = ground$NAME_1, NAME_2 = ground$NAME_2, ID = ground[[ID]])))
    metadata_D$MetaDVar <- rownames(metadata_D)
    colnames(metadata_D) <- colnames(RelativeHumidity_points)
    RelativeHumidity_points <- rbind(metadata_D, RelativeHumidity_points)
  }
  
  
  x <- gsub("_", "-",RelativeHumidity_points$MetaDVar[6:nrow(RelativeHumidity_points)])
  y <- as.Date(x, format="%d-%m-%Y")
  
  Date <- day(y)
  Month <- month(y)
  Year <- year(y)
  
  Date <- ifelse(Date < 10, paste(0,Date,sep=""), as.character(Date))
  Month <- ifelse(Month < 10, paste(0,Month,sep=""), as.character(Month))
  
  
  RelativeHumidity_points$Date <- c(rep(NA, 5), Date)
  RelativeHumidity_points$Month <- c(rep(NA, 5), Month)
  RelativeHumidity_points$Year <- c(rep(NA, 5), Year)
  
  # 4.4. writing the output ####
  
  # Check if the directory exists
  Planting_month_date <- gsub("-", "_", Planting_month_date)
  fname_rain <- ifelse(AOI == "TRUE", paste(varName, "_4CM_AOI_", Planting_month_date, "_", dataSource, ".RDS",sep=""), paste(varName,"_4CM_trial_", dataSource, ".RDS", sep=""))
  
  saveRDS(object = RelativeHumidity_points, file=paste(pathOut1, fname_rain, sep="/"))
  saveRDS(object = RelativeHumidity_points, file=paste(pathOut2, fname_rain, sep="/"))
  
  return(RelativeHumidity_points)
}



